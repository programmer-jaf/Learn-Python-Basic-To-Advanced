# What is a variable?
# Variable is a name given to memory location in a program. Variables are container to store values.

# Integer
a = 10
print(a)
# Floating Point Number
b = 51.4
print(b)
# String
c = "Hello, World!"
print(c)
# Boolean
d = True
print(d)
e = False
print(e)
# None
f = None
print(f)
