# type-casting
a = 34
print(a)
print(type(a))
# change the integer to string

a = str(a)

print(a)

print(type(a))

# change the string to float
b = "354"

print(b)
print(type(b))

# change string to float
b = float(b)
print(b)
print(type(b))

# change the float to integer
c = 5.4624
print(c)
print(type(c))

# change float to integer

c = int(c)
print(c)
print(type(c))
